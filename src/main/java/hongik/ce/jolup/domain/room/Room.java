package hongik.ce.jolup.domain.room;

import hongik.ce.jolup.domain.Time;
import hongik.ce.jolup.domain.join.Join;
import hongik.ce.jolup.dto.RoomDto;
import lombok.*;

import javax.persistence.*;
import java.util.ArrayList;
import java.util.List;
import java.util.stream.Collectors;

@Entity
@Getter
@NoArgsConstructor(access = AccessLevel.PROTECTED)
@Table(name = "room")
@ToString(of = {"id", "title", "roomType", "headCount"})
public class Room extends Time {
    /*
    drop table if exists room CASCADE;
    create table room(
    room_id bigint generated by default as identity,
    subject varchar(255) not null,
    room_type enum('LEAGUE', 'TOURNAMENT') not null,
    mem_num bigint not null,
    primary key(room_id)
    );
    */
    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    @Column(name = "room_id")
    private Long id;

    @Column(length = 30, nullable = false)
    private String title;

    @Column(nullable = false)
    @Enumerated(EnumType.STRING)
    private RoomType roomType;

    @Column(nullable = false)
    private Long headCount;

    @OneToMany(mappedBy = "room", cascade = CascadeType.ALL)
    private List<Join> joins = new ArrayList<>();

    @Builder
    public Room(Long id, String title, RoomType roomType, Long headCount/*, List<Join> joins*/) {
        this.id = id;
        this.title = title;
        this.roomType = roomType;
        this.headCount = headCount;
//        this.joins = joins;
    }

    public RoomDto toDto() {
        return RoomDto.builder()
                .id(id)
                .title(title)
                .roomType(roomType)
                .headCount(headCount)
//                .joinDtos(joins.stream().map(Join::toDto).collect(Collectors.toList()));
                .build();
    }

    public Room update(String title, RoomType roomType, Long headCount) {
        this.title = title;
        this.roomType = roomType;
        this.headCount = headCount;
        return this;
    }
}
